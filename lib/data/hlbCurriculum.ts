/** Look up a curriculum stage by its ID. */
export function getStageById(id: number): HLBCurriculumStage | undefined {
  return hlbCurriculum.find((s) => s.id === id);
}

/** Get all stages belonging to a given block. */
export function getStagesByBlock(block: string): HLBCurriculumStage[] {
  return hlbCurriculum.filter((s) => s.block === block);
}

/** Ordered list of unique block names. */
export const blockNames: string[] = Array.from(
  new Set(hlbCurriculum.map((s) => s.block)),
);
